#!/usr/bin/env bash
# Generate docs/ATLAS.md from frontmatter across canon/, active/, state/
# Run: ./scripts/generate-atlas.sh
# Also runs as pre-commit hook to keep atlas fresh.

set -euo pipefail
DOCS_DIR="$(cd "$(dirname "$0")/../docs" && pwd)"
ATLAS="$DOCS_DIR/ATLAS.md"

# Parse frontmatter field from a file. Usage: fm_field <file> <field>
fm_field() {
  local file="$1" field="$2"
  # Match "Field: value" in the first 15 lines (frontmatter zone)
  head -15 "$file" | sed -n "s/^${field}: *//p" | head -1
}

# Extract title from first H1 line
fm_title() {
  head -5 "$1" | sed -n 's/^# *//p' | head -1
}

# Collect doc metadata into arrays
declare -a DOC_PATHS DOC_TITLES DOC_KINDS DOC_STATUSES DOC_PRIORITIES DOC_REQUIRES
idx=0

scan_dir() {
  local dir="$1"
  if [ ! -d "$dir" ]; then return; fi
  while IFS= read -r -d '' file; do
    local rel="${file#$DOCS_DIR/}"
    local title kind status priority requires
    title="$(fm_title "$file")"
    kind="$(fm_field "$file" "Kind")"
    status="$(fm_field "$file" "Status")"
    priority="$(fm_field "$file" "Priority")"
    requires="$(fm_field "$file" "Requires")"
    [ -z "$title" ] && title="$(basename "$file" .md)"
    [ -z "$priority" ] && priority="5"
    DOC_PATHS[$idx]="$rel"
    DOC_TITLES[$idx]="$title"
    DOC_KINDS[$idx]="$kind"
    DOC_STATUSES[$idx]="$status"
    DOC_PRIORITIES[$idx]="$priority"
    DOC_REQUIRES[$idx]="$requires"
    idx=$((idx + 1))
  done < <(find "$dir" -name '*.md' -print0 | sort -z)
}

scan_dir "$DOCS_DIR/canon"
scan_dir "$DOCS_DIR/active"
scan_dir "$DOCS_DIR/state"

# --- Emit ATLAS.md ---
{
cat <<'HEADER'
# ChoirOS Atlas

> Auto-generated by `scripts/generate-atlas.sh`. Do not edit manually.
> Re-run: `just atlas` or commit (pre-commit hook).

## System at a Glance

ChoirOS is a supervision-tree-first multi-agent runtime. Local-first reliability,
then OVH US-East bootstrap with strict control-plane secrets boundaries.

**Quick start:**
```bash
just local-build-ui && just dev
# Open http://127.0.0.1:9090/login
```

HEADER

# --- Active: In Progress (listed first — this is where attention goes) ---
echo "## Active (In Progress)"
echo ""

# Sort active docs by priority (lower = higher priority)
active_indices=()
for i in $(seq 0 $((idx - 1))); do
  case "${DOC_PATHS[$i]}" in active/*) active_indices+=("$i") ;; esac
done

# Simple insertion sort by priority
for ((a=1; a<${#active_indices[@]}; a++)); do
  key=${active_indices[$a]}
  b=$((a - 1))
  while [ $b -ge 0 ] && [ "${DOC_PRIORITIES[${active_indices[$b]}]}" -gt "${DOC_PRIORITIES[$key]}" ] 2>/dev/null; do
    active_indices[$((b + 1))]=${active_indices[$b]}
    b=$((b - 1))
  done
  active_indices[$((b + 1))]=$key
done

has_active=false
for i in "${active_indices[@]}"; do
  has_active=true
  local_req="${DOC_REQUIRES[$i]}"
  req_str=""
  [ -n "$local_req" ] && [ "$local_req" != "[]" ] && req_str=" | Requires: $local_req"
  printf -- "- **[P%s]** %s — %s (%s)%s  \n  \`docs/%s\`\n" \
    "${DOC_PRIORITIES[$i]}" "${DOC_TITLES[$i]}" "${DOC_KINDS[$i]:-?}" "${DOC_STATUSES[$i]:-?}" "$req_str" "${DOC_PATHS[$i]}"
done
$has_active || echo "_No active docs._"
echo ""

# --- State: Execution Artifacts ---
echo "## State (Snapshots & Reports)"
echo ""
has_state=false
for i in $(seq 0 $((idx - 1))); do
  case "${DOC_PATHS[$i]}" in state/*) has_state=true ;; *) continue ;; esac
  printf -- "- %s — %s (%s)  \n  \`docs/%s\`\n" \
    "${DOC_TITLES[$i]}" "${DOC_KINDS[$i]:-?}" "${DOC_STATUSES[$i]:-?}" "${DOC_PATHS[$i]}"
done
$has_state || echo "_No state docs yet._"
echo ""

# --- Canon: The Truth ---
echo "## Canon (Accepted / Operational)"
echo ""
has_canon=false
for i in $(seq 0 $((idx - 1))); do
  case "${DOC_PATHS[$i]}" in canon/*) has_canon=true ;; *) continue ;; esac
  printf -- "- **%s** — %s (%s)  \n  \`docs/%s\`\n" \
    "${DOC_TITLES[$i]}" "${DOC_KINDS[$i]:-?}" "${DOC_STATUSES[$i]:-?}" "${DOC_PATHS[$i]}"
done
$has_canon || echo "_No canon docs yet._"
echo ""

# --- Dependency Graph ---
echo "## Dependency Graph"
echo ""
echo '```'
has_deps=false
for i in $(seq 0 $((idx - 1))); do
  local_req="${DOC_REQUIRES[$i]}"
  if [ -n "$local_req" ] && [ "$local_req" != "[]" ]; then
    has_deps=true
    # Clean brackets: [ADR-0013, ADR-0014] → ADR-0013, ADR-0014
    clean_req="$(echo "$local_req" | tr -d '[]')"
    printf "%s → %s\n" "$clean_req" "${DOC_TITLES[$i]}"
  fi
done
$has_deps || echo "(no dependencies declared yet)"
echo '```'
echo ""

# --- Stats ---
canon_count=0; active_count=0; state_count=0
for i in $(seq 0 $((idx - 1))); do
  case "${DOC_PATHS[$i]}" in
    canon/*) canon_count=$((canon_count + 1)) ;;
    active/*) active_count=$((active_count + 1)) ;;
    state/*) state_count=$((state_count + 1)) ;;
  esac
done
printf "*Generated %s — %d canon, %d active, %d state docs.*\n" \
  "$(date +%Y-%m-%d)" "$canon_count" "$active_count" "$state_count"

} > "$ATLAS"

echo "atlas: wrote $ATLAS ($idx docs scanned)"
